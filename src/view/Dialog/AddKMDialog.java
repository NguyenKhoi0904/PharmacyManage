    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // Ki·ªÉm tra c√°c tr∆∞·ªùng c∆° b·∫£n
        String tenKM = tfTenKM.getText();
        if (!ValidationUtils.isNotEmpty(tenKM)) {
            JOptionPane.showMessageDialog(this, "T√™n khuy·∫øn m√£i kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
            return;
        }
        
        String loaiKM = cbLoaiKM.getSelectedItem().toString();
        if (!ValidationUtils.isNotEmpty(loaiKM)) {
            JOptionPane.showMessageDialog(this, "Lo·∫°i khuy·∫øn m√£i kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
            return;
        }
        
        if (!ValidationUtils.isValidDecimal(tfGiaTri.getText())) {
            JOptionPane.showMessageDialog(this, "Gi√° tr·ªã khuy·∫øn m√£i ph·∫£i l√† s·ªë!");
            return;
        }
        BigDecimal giaTri = new BigDecimal(tfGiaTri.getText().trim());
        
        // üîπ L·∫•y gi√° tr·ªã ng√†y t·ª´ 2 date chooser
        Date startDate = startDateChooser.getDate();
        Date endDate = endDateChooser.getDate();

        // üîπ Ki·ªÉm tra null
        if (startDate == null || endDate == null) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn ƒë·∫ßy ƒë·ªß ng√†y b·∫Øt ƒë·∫ßu v√† ng√†y k·∫øt th√∫c!");
            return;
        }

        // üîπ Chuy·ªÉn sang LocalDate ƒë·ªÉ so s√°nh (chu·∫©n h∆°n)
        LocalDate start = startDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate end = endDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // üîπ Ki·ªÉm tra logic ng√†y
        if (start.isAfter(end)) {
            JOptionPane.showMessageDialog(this, "Ng√†y b·∫Øt ƒë·∫ßu kh√¥ng ƒë∆∞·ª£c sau ng√†y k·∫øt th√∫c!");
            return;
        }

        // üîπ N·∫øu t·∫•t c·∫£ h·ª£p l·ªá
        try {
            int trangThai = cbTrangThai.getSelectedIndex(); // 0: tam ngung, 1: hoat dong
            
            // üîπ Chuy·ªÉn t·ª´ util.Date sang sql.Date
            java.sql.Date sqlStartDate = new java.sql.Date(startDate.getTime());
            java.sql.Date sqlEndDate = new java.sql.Date(endDate.getTime());
            
            // üîπ T·∫°o DTO
            KhuyenMaiDTO newKM = new KhuyenMaiDTO(
                    tenKM,
                    loaiKM,
                    giaTri,
                    tfDieuKien.getText(),
                    sqlStartDate,
                    sqlEndDate,
                    trangThai // tr·∫°ng th√°i m·∫∑c ƒë·ªãnh l√† k√≠ch ho·∫°t
            );

            // üîπ G·ªçi BUS ƒë·ªÉ th√™m (n·∫øu c√≥)
            if(BUSManager.khuyenMaiBUS.addKhuyenMai(newKM)) {
                JOptionPane.showMessageDialog(this, "Th√™m khuy·∫øn m√£i th√†nh c√¥ng!");
                saved = true;
                dispose(); // ƒë√≥ng 
            }
            else {
                JOptionPane.showMessageDialog(this, "Th√™m khuy·∫øn m√£i th·∫•t b·∫°i!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "D·ªØ li·ªáu kh√¥ng h·ª£p l·ªá: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

