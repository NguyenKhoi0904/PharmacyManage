    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // TODO add your handling code here:
        try{
            String ten = jTextField_Ten.getText().trim();
            if(ten.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Nhập dữ liệu phù hợp");
                return;
            }
            
            DanhMucThuocDTO dmt_DTO = new DanhMucThuocDTO(this.getMa_DMT(), ten, 1);
            boolean flag = DanhMucThuocBUS.getInstance().updateDanhMucThuoc(dmt_DTO);
            if (flag){
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin danh mục thuốc thành công");
                dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin danh mục thuốc thất bại");
            }
        }catch (Exception ex){
            JOptionPane.showMessageDialog(rootPane, "LỖI Ở SuaDanhMucThuoc.java");
        }

    }//GEN-LAST:event_jButton_UpdateActionPerformed

