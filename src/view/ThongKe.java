    private void jComboBox_YearItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox_YearItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED){
            loadThongKeTheoNam();
            loadBieuDoBanThuocTheoThang();
        }
    }//GEN-LAST:event_jComboBox_YearItemStateChanged

    private void loadThongKeTheoNam(){
        int year = Integer.parseInt(jComboBox_Year.getSelectedItem().toString());
        
        ArrayList<ThongKeDTO> list = ThongKeBUS.getInstance().thongKeTheoNam(year);
        System.out.println(list);
        
        DefaultTableModel model = (DefaultTableModel)jTable_ThongKe.getModel();
        model.setRowCount(0);
        
        for(ThongKeDTO tk : list){
            Object[] row = {
                tk.getThang(),
                tk.getSoLuong(),
                tk.getDoanhThu()
            };
            model.addRow(row);
        }
    }
    
    private void loadBieuDoBanThuocTheoThang(){
        int year = Integer.parseInt(jComboBox_Year.getSelectedItem().toString());
        
        Map<Integer, Integer> map = ThongKeBUS.getInstance().thongKeTheoThang(year);
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (int month = 1; month <= 12; month++) {
            int value = map.getOrDefault(month, 0);  // nếu không có thì trả 0
            dataset.addValue(value, "Số lượng", Integer.toString(month));
        }

        
        JFreeChart chart = ChartFactory.createBarChart(
                "Số lượng thuốc bán theo tháng",
                "Tháng",
                "Số lượng",
                dataset
        );
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(jPanel_Chart.getWidth(), jPanel_Chart.getHeight()));
        
        jPanel_Chart.removeAll();
        jPanel_Chart.setLayout(new java.awt.BorderLayout());
        jPanel_Chart.add(chartPanel, BorderLayout.CENTER);
        jPanel_Chart.revalidate();
        jPanel_Chart.repaint();
    }
    
